// This program demonstrates the usage of various data types in Dart: int, double, String, List, and Map.

void main() {
  int age = 30;
  print("My age is $age.");

  double pi = 3.14159;
  print("The value of pi is $pi.");

  String name = "Alice";
  print("My name is $name.");

  List<String> fruits = ["apple", "banana", "orange"];
  print("My favorite fruits are: $fruits.");

  Map<String, int> person = {"age": 30, "height": 175};
  print("Person's information: $person");
}
